public class ConditionalOperators
{
   public static void main(String[] args)
   {
      int num1 = 1;
      int num2 = 2;

      System.out.println("Is number1 greater than number2? " + (num1 > num2));
      System.out.println("Is number1 less than number2? " + (num1 < num2));
      System.out.println("Is number1 greater than or equal to number2? " + (num1 >= num2));
      System.out.println("Is number1 less than or equal number2? " + (num1 <= num2));
      System.out.println("Is number1 equal to number2? " + (num1 == num2));
      System.out.println("Is number1 NOT equal to number2? " + (num1 != num2));
   }
}
